
public class Toisto {
	/*
	 * Tämä ohjelma, nimeltään Toisto.java, tulostaa kokonaisluvut väliltä 1-100
	 * omille riveilleen. Ohjelma käyttää for-toistoa, ja jokainen luku tulostetaan
	 * omalle rivilleen.
	 */
	public static void main(String[] args) {
		for (int i = 1; i <= 100; i++)
			System.out.println(i);
	}
}
